#include "libft.h"

int	ft_atoi(const char *nb)
{
	int	i;
	int	s;
	int	n;

	i = 0;
	s = 1;
	n = 0;
	while (nb[i] == '+' || nb[i] == '-')
	{
		if (nb[i] == '-')
		{
			s *= -1;
		}
		i++;
	}
	while (nb[i] <= '9' && nb[i] >= '0')
	{
		n = (n * 10) + (nb[i] - '0');
		i++;
	}
	return (n * s);
}