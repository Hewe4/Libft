#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*p;
	size_t	l;
	int		i;

	i = 0;
	l = ft_strlen(s);
	p = (char *)malloc (sizeof (char) * (l + 1));
	if (!p)
		return (NULL);
	while (s[i] != '\0')
	{
		p[i] = s[i];
		i++;
	}
	p[i] = "\0";
	return (p);
}
